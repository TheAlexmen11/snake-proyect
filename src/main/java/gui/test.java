    public void mostrarPanel(String nombrePanel) {
        
        if(nombrePanel.equals("JUEGO")){
            setSize(500, 520);
        }
        cardLayout.show(contenedor, nombrePanel);

        Component actual = null;
        for (Component comp : contenedor.getComponents()) {
            if (comp.isVisible()) {
                actual = comp;
                
                break;
            }
        }

        if (actual != null) {
            actual.requestFocusInWindow(); // ðŸ”‘ Esto permite que MenuPanel reciba las teclas
        }
    }
    
    
    
